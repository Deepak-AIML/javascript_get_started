# Example 1: Print the first 10 natural numbers using for loop.
n = 11
for i in range(n):
    print(i)
    
# program to print all the even numbers within the given range.
for i in range(n):
    if i%2==0:
        print(i)
        
# program to calculate the sum of all numbers from 1 to a given number
sum = 0
for i in range(n + 1):
    sum += i
print(sum)
